#include "libft.h"

void	ft_putendl(char *s)
{
	ft_putendl_fd(s, 1);
}

void	ft_putendl_fd(char *s, int fd)
{
	int i;

	i = 0;
	if (!s)
		return ;
	while (s[i])
	{
		write(fd, &s[i], 1);
		i++;
	}
	write(fd, "\n", 1);
}
